// updcrc16 implements the lrzsz CRC-16 update formula.
// This matches lrzsz's: crctab[((crc >> 8) & 255)] ^ (crc << 8) ^ cp
func updcrc16(b byte, crc uint16) uint16 {
	return crctab[byte(crc>>8)] ^ (crc << 8) ^ uint16(b)
}

// crc16Calc computes ZMODEM CRC-16 with proper finalization.
// Feeds data through updcrc, then feeds two zero bytes (finalization).
// The finalized value is what gets transmitted on the wire.
func crc16Calc(data []byte) uint16 {
	var crc uint16
	for _, b := range data {
		crc = updcrc16(b, crc)
	}
	crc = updcrc16(0, updcrc16(0, crc))
	return crc
}

// crc16Update incrementally updates a CRC-16 value with additional data.
// Does NOT apply finalization â€” caller must call crc16Finalize when done.
func crc16Update(crc uint16, data []byte) uint16 {
	for _, b := range data {
		crc = updcrc16(b, crc)
	}
	return crc
}

// crc16Finalize applies the two-zero-byte finalization to a running CRC-16.
func crc16Finalize(crc uint16) uint16 {
	return updcrc16(0, updcrc16(0, crc))
}

// crc16Verify checks if data (including the 2 appended CRC bytes) verifies correctly.
// Feeds all bytes through updcrc WITHOUT finalization. Result should be 0.
// Per lrzsz: after feeding data + CRC_hi + CRC_lo through updcrc, check == 0.
func crc16Verify(dataWithCRC []byte) bool {
	var crc uint16
	for _, b := range dataWithCRC {
		crc = updcrc16(b, crc)
	}
	return crc == 0
}

// CRC-32 uses the standard IEEE polynomial (same as ZMODEM spec).
func crc32Calc(data []byte) uint32 {
	return crc32.ChecksumIEEE(data)
}

// crc32Update incrementally updates a CRC-32 value with additional data.
func crc32Update(crc uint32, data []byte) uint32 {
	return crc32.Update(crc, crc32.IEEETable, data)
}

// crc32VerifyMagic is the expected result when feeding data+CRC32 through crc32.ChecksumIEEE.
// lrzsz checks raw CRC against 0xDEBB20E3 (no final XOR). Go's ChecksumIEEE applies
// final XOR (^0xFFFFFFFF), so the magic becomes 0xDEBB20E3 ^ 0xFFFFFFFF = 0x2144DF1C.
const crc32VerifyMagic = 0x2144DF1C

// crc32Verify checks if data (including 4 appended CRC bytes, LE) verifies correctly.
func crc32Verify(dataWithCRC []byte) bool {
	return crc32.ChecksumIEEE(dataWithCRC) == crc32VerifyMagic
}

// appendCRC16 appends the CRC-16 value as 2 bytes (big-endian) to the data.
func appendCRC16(data []byte, crc uint16) []byte {
	return append(data, byte(crc>>8), byte(crc&0xff))
}

// appendCRC32 appends the CRC-32 value as 4 bytes (little-endian) to the data.
func appendCRC32(data []byte, crc uint32) []byte {
	var buf [4]byte
	binary.LittleEndian.PutUint32(buf[:], crc)
	return append(data, buf[:]...)
}
